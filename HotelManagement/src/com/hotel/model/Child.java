package com.hotel.model;

public class Child {
    
    private int child_ID;
    private String child_Name;
    private String child_Age;
    private int family_ID;

    /**
     * @return the child_ID
     */
    public int getChild_ID() {
        return child_ID;
    }

    /**
     * @param child_ID the child_ID to set
     */
    public void setChild_ID(int child_ID) {
        this.child_ID = child_ID;
    }

    /**
     * @return the child_Name
     */
    public String getChild_Name() {
        return child_Name;
    }

    /**
     * @param child_Name the child_Name to set
     */
    public void setChild_Name(String child_Name) {
        this.child_Name = child_Name;
    }

    /**
     * @return the child_Age
     */
    public String getChild_Age() {
        return child_Age;
    }

    /**
     * @param child_Age the child_Age to set
     */
    public void setChild_Age(String child_Age) {
        this.child_Age = child_Age;
    }

    /**
     * @return the family_ID
     */
    public int getFamily_ID() {
        return family_ID;
    }

    /**
     * @param family_ID the family_ID to set
     */
    public void setFamily_ID(int family_ID) {
        this.family_ID = family_ID;
    }
}
